CREATE TABLE artist
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) NOT NULL PRIMARY KEY,	
	artist_name VARCHAR(255) NOT NULL UNIQUE
);

CREATE TABLE album
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) NOT NULL PRIMARY KEY,		
	artist_id INTEGER NOT NULL FOREIGN KEY REFERENCES artist (id),
	album_title VARCHAR(255) NOT NULL,
	album_year SMALLINT NOT NULL
);

CREATE TABLE song
(
    id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) NOT NULL PRIMARY KEY,		
	album_id INTEGER NOT NULL FOREIGN KEY REFERENCES album (id),
	song_title VARCHAR(255) NOT NULL,
	track SMALLINT NOT NULL,	
	disc SMALLINT NOT NULL
);